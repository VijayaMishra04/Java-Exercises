package Oct.Ex_15_10_2024_IncrementDecrement;

public class Lab020_15102024_Task3 {
    public static void main(String[] args) {
        int a = 20;

        System.out.println(--a + a++ + a--);
        System.out.println(a);
        // A-> --a = 19
        // B-> a++ = 19
        // C-> a-- = 20
        // A+B+C =19+19+20 =58
        // a= 19
    }
}
