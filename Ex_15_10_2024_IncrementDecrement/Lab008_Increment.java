package Oct.Ex_15_10_2024_IncrementDecrement;

public class Lab008_Increment {
    public static void main(String[] args) {
        //pre-increment
        int a = 17;
        int result = ++a;
        System.out.println(result);
        System.out.println(a);
        // line no. |Expression (Result) | a
        // 6 | NA | 17
        // 7 | 18 | 18
    }
}
