package Oct.Ex_15_10_2024_IncrementDecrement;

public class Lab006_Increment {
    public static void main(String[] args) {
        //Pre-Increment --> ++operand
        //Value incremented 1st then stored in the result
        int a = 12;
        int b = ++a;
        System.out.print("Result (b) : " + b);
        System.out.print("\n a : " + a);
        //Expression and result table
        // line no | operand value (a)| result (b)
        // 7 | 12 | NA
        // 8 | 13 | 13

    }
}
